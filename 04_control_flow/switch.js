//switch cases

const month ="apr"

switch (month) {
    case 1:
        console.log("january")
        break;
    case 2:
        console.log("Febuary")
        break;
    case 3:
        console.log("march")
        break;
    case "apr":
        console.log("april")
        break;

    default:
        console.log("default case match");
        
        break;
}
//if "break was not there the all the code would be run except defalt"
